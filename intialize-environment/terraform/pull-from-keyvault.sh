#!/bin/bash
# ============================================================
# Pull secrets from Azure Key Vault and generate terraform.tfvars
# Usage: ./pull-from-keyvault.sh [key-vault-name]
# ============================================================

set -e

# Configuration
KEY_VAULT_NAME="${1:-$KEY_VAULT_NAME}"
OUTPUT_FILE="terraform.tfvars"
SECRET_PREFIX="tfvar-"

echo "=========================================="
echo "Pulling variables from Azure Key Vault"
echo "=========================================="
echo "Key Vault: $KEY_VAULT_NAME"
echo "Output: $OUTPUT_FILE"
echo ""

# Check Azure CLI
if ! command -v az &> /dev/null; then
    echo "ERROR: Azure CLI not installed"
    exit 1
fi

# Check if Key Vault exists
if ! az keyvault show --name "$KEY_VAULT_NAME" &> /dev/null; then
    echo "ERROR: Key Vault '$KEY_VAULT_NAME' not found!"
    exit 1
fi

# Create output file with header
cat > "$OUTPUT_FILE" << 'EOF'
# ============================================================
# Auto-generated from Azure Key Vault
# DO NOT EDIT THIS FILE MANUALLY
# Generated: $(date)
# ============================================================

EOF

# Add timestamp
echo "# Generated: $(date '+%Y-%m-%d %H:%M:%S')" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

# Get all secrets with prefix
echo "Fetching secrets..."

# Fetch all secrets
secrets=$(az keyvault secret list \
    --vault-name "$KEY_VAULT_NAME" \
    --query "[?starts_with(name, '$SECRET_PREFIX')].name" \
    --output tsv 2>/dev/null) || secrets=""

if [ -z "$secrets" ]; then
    echo "WARNING: No secrets found with prefix '$SECRET_PREFIX'"
    echo "Run push-to-keyvault.sh first to upload variables."
fi

COUNT=0
while IFS= read -r secret_name; do
    [ -z "$secret_name" ] && continue
    
    # Remove prefix
    var_name="${secret_name#$SECRET_PREFIX}"
    
    # Get secret value
    value=$(az keyvault secret show \
        --vault-name "$KEY_VAULT_NAME" \
        --name "$secret_name" \
        --query "value" \
        --output tsv 2>/dev/null) || value=""
    
    if [ -n "$value" ]; then
        # Mask sensitive values
        if [[ "$var_name" =~ password|secret ]]; then
            echo "# $var_name = <sensitive>" >> "$OUTPUT_FILE"
            echo "${var_name} = \"$value\"" >> "$OUTPUT_FILE"
            echo "  ✓ $var_name = ********"
        else
            echo "$var_name = \"$value\"" >> "$OUTPUT_FILE"
            echo "  ✓ $var_name = $value"
        fi
        ((COUNT++))
    fi
done <<< "$secrets"

echo ""
echo "=========================================="
echo "COMPLETE!"
echo "=========================================="
echo "Secrets pulled: $COUNT"
echo "Output file: $OUTPUT_FILE"
echo ""
echo "Note: Sensitive variables (passwords) are visible in the file."
echo "Add 'terraform.tfvars' to .gitignore if not already present."
echo ""
echo "To run Terraform:"
echo "  terraform init"
echo "  terraform plan"